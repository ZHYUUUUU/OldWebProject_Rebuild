package com.xuebo.bean;

public class Tribune {
	
	private String tribuneid;
	
	private String creator;
	
	private int creatorstate;
	
	private String studentid;
	
	private String expertid;
	
	private String creatorimage;
	
	private String createdate;
	
	private String tribunetitle;
	
	private String tribunetheme;
	
	private String tribunecontent;

	public Tribune(){
		
	}
	
	public String getTribuneid() {
		return tribuneid;
	}

	public void setTribuneid(String tribuneid) {
		this.tribuneid = tribuneid;
	}

	public String getCreator() {
		return creator;
	}

	public void setCreator(String creator) {
		this.creator = creator;
	}

	public int getCreatorstate() {
		return creatorstate;
	}

	public void setCreatorstate(int creatorstate) {
		this.creatorstate = creatorstate;
	}

	public String getStudentid() {
		return studentid;
	}

	public void setStudentid(String studentid) {
		this.studentid = studentid;
	}

	public String getExpertid() {
		return expertid;
	}

	public void setExpertid(String expertid) {
		this.expertid = expertid;
	}

	public String getCreatorimage() {
		return creatorimage;
	}

	public void setCreatorimage(String creatorimage) {
		this.creatorimage = creatorimage;
	}

	public String getCreatedate() {
		return createdate;
	}

	public void setCreatedate(String createdate) {
		this.createdate = createdate;
	}

	public String getTribunetitle() {
		return tribunetitle;
	}

	public void setTribunetitle(String tribunetitle) {
		this.tribunetitle = tribunetitle;
	}

	public String getTribunetheme() {
		return tribunetheme;
	}

	public void setTribunetheme(String tribunetheme) {
		this.tribunetheme = tribunetheme;
	}

	public String getTribunecontent() {
		return tribunecontent;
	}

	public void setTribunecontent(String tribunecontent) {
		this.tribunecontent = tribunecontent;
	}
	

}
