package com.xuebo.bean;

public class Organization {

	private int statecode;
	
	private String organizationid;
	
	private String organizationname;
	
	private String organizationclass;
	
	private String organizationadd;
	
	private String organizationaddress;
	
	private String organizationclass_price;
	
	private String organizationintroduce;
	
	private String organizationphone;
	
	private String organizationimage;

	public Organization(){
		
	}
	
	public int getStatecode() {
		return statecode;
	}

	public void setStatecode(int statecode) {
		this.statecode = statecode;
	}

	public String getOrganizationid() {
		return organizationid;
	}

	public void setOrganizationid(String organizationid) {
		this.organizationid = organizationid;
	}

	public String getOrganizationname() {
		return organizationname;
	}

	public void setOrganizationname(String organizationname) {
		this.organizationname = organizationname;
	}

	public String getOrganizationclass() {
		return organizationclass;
	}

	public void setOrganizationclass(String organizationclass) {
		this.organizationclass = organizationclass;
	}

	public String getOrganizationadd() {
		return organizationadd;
	}

	public void setOrganizationadd(String organizationadd) {
		this.organizationadd = organizationadd;
	}

	public String getOrganizationaddress() {
		return organizationaddress;
	}

	public void setOrganizationaddress(String organizationaddress) {
		this.organizationaddress = organizationaddress;
	}

	public String getOrganizationclass_price() {
		return organizationclass_price;
	}

	public void setOrganizationclass_price(String organizationclass_price) {
		this.organizationclass_price = organizationclass_price;
	}

	public String getOrganizationintroduce() {
		return organizationintroduce;
	}

	public void setOrganizationintroduce(String organizationintroduce) {
		this.organizationintroduce = organizationintroduce;
	}

	public String getOrganizationphone() {
		return organizationphone;
	}

	public void setOrganizationphone(String organizationphone) {
		this.organizationphone = organizationphone;
	}

	public String getOrganizationimage() {
		return organizationimage;
	}

	public void setOrganizationimage(String organizationimage) {
		this.organizationimage = organizationimage;
	}
	
}
